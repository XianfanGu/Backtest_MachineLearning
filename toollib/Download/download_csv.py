def _request_csv(symbol, req_type):
    try:
        urllib.request.urlretrieve(QUERY_URL_CSV.format(REQUEST_TYPE=req_type, KEY=API_KEY, SYMBOL=symbol), 'csv/daily/'+symbol+'.csv')
    except urllib.error.HTTPError as ex:
        print('Problem:', ex)



def format_file(symbol):
    body = []
    try:
        with open('csv/daily/'+symbol+'.csv', "r") as inp:
            START_DATE = datetime.strptime("2006-10-01", DT_FORMAT)
            END_DATE = datetime.strptime("2019-04-18", DT_FORMAT)
            #print(START_DATE)

            inp.seek(0)
            lastrow = None
            for lastrow in csv.reader(inp): pass
            if(datetime.strptime(lastrow[0], DT_FORMAT)>START_DATE):
                os.remove('csv/daily/'+symbol+'.csv')
                print("File Removed!",'  '+'csv/daily/'+symbol+'.csv')
                return False
            inp.seek(0)
            for row in csv.reader(inp):
                if(row[0]=='timestamp'):
                    body.append(row)
                    continue
                DATE = datetime.strptime(row[0], DT_FORMAT)
                #print(DATE)
                if(DATE >= START_DATE) and (DATE <= END_DATE):
                    body.append(row)

        with open('csv/daily/'+symbol+'.csv', "w") as out:
            writer = csv.writer(out)
            for row in body:
                writer.writerow(row)
        return True

    except Exception as error:
        pass

    os.remove('csv/daily/' + symbol + '.csv')
    print("File Removed!", '  '+'csv/daily/' + symbol + '.csv')
    print('Caught this error: format is not correct')
    return False

